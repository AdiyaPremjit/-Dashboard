import pymc as pm
import arviz as az
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt

# -----------------------------
# 1. Load data
# -----------------------------
df = pd.read_csv("final_dataset_pre_bayesian.csv")

# Keep required columns
df = df[["planet_count", "st_met", "st_teff", "st_rad"]].dropna()

# -----------------------------
# 2. Feature scaling (CRITICAL)
# -----------------------------
for col in ["st_met", "st_teff", "st_rad"]:
    df[col] = (df[col] - df[col].mean()) / df[col].std()

# Convert to numpy
X_met = df["st_met"].values
X_teff = df["st_teff"].values
X_rad = df["st_rad"].values
y = df["planet_count"].values

# -----------------------------
# 3. Bayesian Model (SAFE)
# -----------------------------
with pm.Model() as model:

    # Priors (tight to prevent explosion)
    beta0 = pm.Normal("beta0", mu=0, sigma=1)
    beta_met = pm.Normal("beta_met", mu=0, sigma=1)
    beta_teff = pm.Normal("beta_teff", mu=0, sigma=1)
    beta_rad = pm.Normal("beta_rad", mu=0, sigma=1)

    # Linear model
    linear = beta0 + beta_met*X_met + beta_teff*X_teff + beta_rad*X_rad

    # Safe Poisson rate
    lambda_ = pm.math.exp(linear)
    lambda_ = pm.math.clip(lambda_, 1e-6, 1e3)

    # Likelihood
    y_obs = pm.Poisson("y_obs", mu=lambda_, observed=y)

    # Sampling
    trace = pm.sample(1000, tune=1000, target_accept=0.9, random_seed=42)

    # Posterior Predictive
    ppc = pm.sample_posterior_predictive(trace, var_names=["y_obs"], random_seed=42)

# -----------------------------

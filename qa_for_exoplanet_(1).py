def bootstrap_slope(x, y, n_boot=500):
    slopes = []
    n = len(x)

    for _ in range(n_boot):
        idx = np.random.choice(n, n, replace=True)
        slope, _, _, _, _ = linregress(x[idx], y[idx])
        slopes.append(slope)

    return np.mean(slopes), np.std(slopes)


    # weighted linear regression in log space
    W = np.diag(weights)
    X = np.vstack([x, np.ones(len(x))]).T
    beta = np.linalg.inv(X.T @ W @ X) @ (X.T @ W @ y)
    slope = beta[0]
    return slope

